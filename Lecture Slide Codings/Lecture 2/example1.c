//Using conversion specifiers in c a program

#include <stdio.h>

int main(void)
{
    printf( "%d\n",  455);
    printf( "%d\n", -455);
    printf( "%i\n",  455);
    printf( "%f\n", 1234567.89);
    printf( "%.2f\n",  3.446);
    printf( "%c\n", 'A' );
    return 0;
                  
} // end of main function
